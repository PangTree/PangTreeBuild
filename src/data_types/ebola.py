def get_official_ebola_name(ebola_code):
    try:
        name, group = _ebola_dictionary[ebola_code]
        return name
    except KeyError:
        return "UNKNOWN EBOLA CODE"


def get_ebola_group_name(ebola_code):
    try:
        name, group_ID = _ebola_dictionary[ebola_code]
        return _ebola_groups_dictionary[group_ID]
    except KeyError:
        return "UNKNOWN EBOLA CODE"


def extract_ebola_code_part(original_ebola_src_name, index):
    try:
        return original_ebola_src_name.split('.')[index]
    except:
        return original_ebola_src_name
        # raise Exception()


def get_ebola_code_for_group(ebola_group_name):
    for group_code, group_name in _ebola_groups_dictionary.items():
        if group_name == ebola_group_name:
            ebola_group_code = group_code

    ebola_codes = []
    for ebola_code, ebola_name in _ebola_dictionary.items():
        if ebola_name[1] == ebola_group_code:
            ebola_codes.append(ebola_code)
    return ebola_codes
